ASSUME CS: CODE, DS:DATA
DATA SEGMENT
ARR DB 99H,12H,56H,45H,36H,11H,0FEH
LEN DW $-ARR
EV DW 1 DUP(?)
DATA ENDS
CODE SEGMENT
START : MOV AX, DATA
	MOV DS,AX
	LEA SI,ARR
	MOV CX,LEN
	MOV BX,0000H
L1    : MOV AL,ARR[SI]
	AND AL,01H
	CMP AL,00H
	JNZ L2
	MOV AH,00H
	MOV AL,ARR[SI]
	ADD BX,AX
L2    : INC SI
	LOOP L1
	MOV EV,BX
	MOV AH,4CH
	INT 21H
CODE ENDS
END START
        
