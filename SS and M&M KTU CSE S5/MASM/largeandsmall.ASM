ASSUME CS: CODE, DS:DATA
DATA SEGMENT
ARR DW 0ABCDH,1234H,4567H,7594H
LEN DW $-ARR
LAR DW 1 DUP(?)
SMA DW 1 DUP(?)
DATA ENDS
CODE SEGMENT
START : MOV AX, DATA
	MOV DS,AX
	LEA SI,ARR
	MOV CX,LEN
	SHR CX,1
	MOV AX,0000H
	MOV BX,ARR[SI]
L1    : CMP AX,ARR[SI]
	JNC L2
	MOV AX,ARR[SI]
L2    : CMP BX,ARR[SI]
	JC L3
	MOV BX,ARR[SI]
L3    : INC SI
	INC SI
	LOOP L1
	MOV LAR,AX
	MOV SMA,BX
	MOV AH,4CH
	INT 21H
CODE ENDS
END START
        
